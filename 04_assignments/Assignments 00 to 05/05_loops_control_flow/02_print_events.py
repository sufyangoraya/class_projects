def main():
    # Initialize the first even number
    even_number = 0

    # While loop to print the first 20 even numbers
    count = 0
    while count < 20:
        print(even_number, end=" ")
        even_number += 2
        count += 1

# Call the main function when "run", no need to edit anything below!
if __name__ == "__main__":
    main()
